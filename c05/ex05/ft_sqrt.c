int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	if (nb < 0)
		return (0);
	else if (nb == 1)
		return (1);
	while (a <= nb / 2)
	{
		if (a * a == nb)
			return (1);
		a++;
	}
	return (0);
}
